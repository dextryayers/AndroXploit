func colorForValue(val string) string {
	if val == "" || val == "?" || strings.Contains(val, "[N/A]") {
		return Red
	}
	lower := strings.ToLower(val)
	if strings.Contains(lower, "error") || strings.Contains(lower, "not") {
		return Yellow
	}
	return Green
}

func printPoint(section, desc, value string) {
	mu.Lock()
	defer mu.Unlock()

	if value == "" {
		value = "?"
	}
	color := colorForValue(value)
	shortVal := value
	if len(shortVal) > 150 {
		shortVal = shortVal[:150]
	}

	fmt.Printf("  "+Green+"[+]"+Reset+" "+White+"%s:"+Reset+" "+color+"%s"+Reset+"\n", desc, shortVal)
	reportFile.WriteString(fmt.Sprintf("  [+] %s: %s\n", desc, shortVal))
	pointCount++
}

