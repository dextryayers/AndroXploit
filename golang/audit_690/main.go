func gpu() {
	printPoint("GPU", "GPU Renderer", getProp("ro.hardware.gralloc"))
	printPoint("GPU", "OpenGL ES Version", getProp("ro.opengles.version"))
	printPoint("GPU", "Vulkan API", detectVulkan())
	printPoint("GPU", "EGL Config", getProp("ro.egl.config"))
	printPoint("GPU", "EGL HW", getProp("ro.hardware.egl"))
	printPoint("GPU", "HWUI Renderer", getProp("debug.hwui.renderer"))
	printPoint("GPU", "SF Vsync", getProp("debug.sf.disable_vsync"))

	// Adreno paths
	gpuClk := catFile("/sys/class/kgsl/kgsl-3d0/gpuclk")
	gpuMax := catFile("/sys/class/kgsl/kgsl-3d0/max_gpuclk")
	gpuBusy := catFile("/sys/class/kgsl/kgsl-3d0/gpu_busy_percentage")
	gpuGov := catFile("/sys/class/kgsl/kgsl-3d0/devfreq/governor")
	gpuTherm := catFile("/sys/class/kgsl/kgsl-3d0/thermal_pwrlevel")
	gpuAvail := catFile("/sys/class/kgsl/kgsl-3d0/gpu_available_frequencies")

	// Mali paths
	if gpuClk == "" || gpuGov == "" {
		maliDev := shellCmd("ls -d /sys/devices/platform/*mali* /sys/devices/platform/*.mali 2>/dev/null | head -1")
		maliDev = strings.TrimSpace(maliDev)
		if maliDev != "" && !strings.Contains(maliDev, "No such") {
			if gpuClk == "" {
				for _, p := range []string{"/frequency", "/cur_freq", "/clock"} {
					if v := catFile(maliDev + p); v != "" {
						gpuClk = v
						break
					}
				}
			}
			if gpuMax == "" {
				gpuMax = catFile(maliDev + "/max_freq")
			}
			if gpuGov == "" {
				for _, p := range []string{"/dvfs_governor", "/governor"} {
					if v := catFile(maliDev + p); v != "" {
						gpuGov = v
						break
					}
				}
			}
			if gpuBusy == "" {
				for _, p := range []string{"/utilization", "/load"} {
					if v := catFile(maliDev + p); v != "" {
						gpuBusy = v
						break
					}
				}
			}
			if gpuAvail == "" {
				gpuAvail = catFile(maliDev + "/available_frequencies")
			}
		}
		// /sys/kernel/gpu fallback
		if gpuClk == "" {
			gpuClk = catFile("/sys/kernel/gpu/gpu_cur_freq")
		}
		if gpuMax == "" {
			gpuMax = catFile("/sys/kernel/gpu/gpu_max_freq")
		}
		if gpuGov == "" {
			gpuGov = catFile("/sys/kernel/gpu/gpu_governor")
		}
		if gpuBusy == "" {
			gpuBusy = catFile("/sys/kernel/gpu/gpu_busy")
		}
		// GPU thermal zone
		if gpuTherm == "" {
			for i := 0; i < 20; i++ {
				ttype := catFile(fmt.Sprintf("/sys/class/thermal/thermal_zone%d/type", i))
				if strings.Contains(strings.ToLower(ttype), "gpu") {
					gpuTherm = catFile(fmt.Sprintf("/sys/class/thermal/thermal_zone%d/temp", i))
					if temp, err := strconv.Atoi(strings.TrimSpace(gpuTherm)); err == nil {
						gpuTherm = fmt.Sprintf("%.1fC", float64(temp)/1000.0)
					}
					break
				}
			}
		}
	}

	// Governor sanity - reject CPU governors
	knownGPU := []string{"mali", "dvfs", "performance", "powersave", "simple_ondemand", "cooperative"}
	if gpuGov != "" {
		isGPU := false
		gpuGovLower := strings.ToLower(gpuGov)
		for _, kg := range knownGPU {
			if strings.Contains(gpuGovLower, kg) {
				isGPU = true
				break
			}
		}
		if !isGPU {
			gpuGov = ""
		}
	}

	printPoint("GPU", "GPU Cur Freq", ifEmpty(gpuClk, "?"))
	printPoint("GPU", "GPU Max Freq", ifEmpty(gpuMax, "?"))
	printPoint("GPU", "GPU Min Freq", catFile("/sys/class/kgsl/kgsl-3d0/min_gpuclk"))
	printPoint("GPU", "GPU Busy", ifEmpty(gpuBusy, "?"))
	printPoint("GPU", "GPU Governor", ifEmpty(gpuGov, "?"))
	printPoint("GPU", "GPU Thermal Level", ifEmpty(gpuTherm, "?"))
	printPoint("GPU", "GPU Avail Freqs", ifEmpty(gpuAvail, "?"))
	printPoint("GPU", "OpenCL Support", checkFile("/system/vendor/lib/libOpenCL.so"))
	printPoint("GPU", "Gralloc Version", getProp("ro.hardware.gralloc"))
}

