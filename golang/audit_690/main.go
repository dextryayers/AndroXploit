func getFirstProp(keys []string) string {
	for _, k := range keys {
		v := getProp(k)
		if v != "?" && v != "" {
			if v == "1" {
				return "On"
			}
			return v
		}
	}
	return "?"
}

func ifEmpty(s, fallback string) string {
	if s == "" {
		return fallback
	}
	return s
}

func getOr(vals ...string) string {
	for _, v := range vals {
		if v != "" && v != "?" {
			return v
		}
	}
	return "?"
}

