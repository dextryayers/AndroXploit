func extractBatteryField(bat, key string) string {
	re := regexp.MustCompile(fmt.Sprintf(`(?m)^\s*%s\s*[:=]\s*(.+)$`, regexp.QuoteMeta(key)))
	matches := re.FindStringSubmatch(bat)
	if len(matches) > 1 {
		return strings.TrimSpace(matches[1])
	}
	return "?"
}

func extractBatteryTemp(bat string) string {
	v := extractBatteryField(bat, "temperature")
	if v == "" || v == "?" {
		return "?"
	}
	temp, err := strconv.Atoi(v)
	if err != nil {
		return v
	}
	return fmt.Sprintf("%.1fC", float64(temp)/10.0)
}

func extractBatteryVoltage(bat string) string {
	v := extractBatteryField(bat, "voltage")
	if v == "" || v == "?" {
		return "?"
	}
	mv, err := strconv.Atoi(v)
	if err != nil {
		return v + "mV"
	}
	return fmt.Sprintf("%dmV (%.3fV)", mv, float64(mv)/1000.0)
}

