func extractRmnet() string {
	link := shellCmd("ip link")
	re := regexp.MustCompile(`rmnet\d+`)
	matches := re.FindAllString(link, -1)
	if len(matches) > 0 {
		return strings.Join(matches, ", ")
	}
	return "?"
}

