func checkADB() bool {
	cmd := exec.Command("adb", "devices")
	out, err := cmd.Output()
	if err != nil {
		fmt.Fprintf(os.Stderr, Red+"[!] ADB not found. Install Android platform tools.\n"+Reset)
		return false
	}
	re := regexp.MustCompile(`(.*?)\s+device$`)
	for _, line := range strings.Split(string(out), "\n") {
		matches := re.FindStringSubmatch(line)
		if len(matches) > 1 {
			deviceSerial = matches[1]
			fmt.Printf(Green+"[+] Device: %s\n\n"+Reset, deviceSerial)
			return true
		}
	}
	fmt.Fprintf(os.Stderr, Red+"[!] No Android device detected.\n"+Reset)
	return false
}

func adb(args ...string) string {
	fullArgs := []string{"-s", deviceSerial}
	fullArgs = append(fullArgs, args...)
	cmd := exec.Command("adb", fullArgs...)
	out, err := cmd.Output()
	if err != nil {
		return ""
	}
	return strings.TrimRight(string(out), "\r\n ")
}

