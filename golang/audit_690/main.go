func extractWifiField(out, field, pattern string) string {
	re := regexp.MustCompile(pattern)
	for _, line := range strings.Split(out, "\n") {
		if strings.Contains(line, field) {
			matches := re.FindStringSubmatch(line)
			if len(matches) > 1 {
				return matches[1]
			}
		}
	}
	return "?"
}

func extractIPAddr() string {
	ip := shellCmd("ip addr show wlan0")
	re := regexp.MustCompile(`inet\s+(\d+\.\d+\.\d+\.\d+)`)
	matches := re.FindStringSubmatch(ip)
	if len(matches) > 1 {
		return matches[1]
	}
	return "?"
}

func extractGateway() string {
	route := shellCmd("ip route show table 0")
	re := regexp.MustCompile(`default\s+via\s+(\d+\.\d+\.\d+\.\d+)`)
	matches := re.FindStringSubmatch(route)
	if len(matches) > 1 {
		return matches[1]
	}
	return getProp("dhcp.wlan0.gateway")
}

